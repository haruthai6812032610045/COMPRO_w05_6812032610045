#include <stdio.h>

int main() {

    // บอกตัวแปร

    char name = 'O';       // ตัวอักษรตัวเเรกเเทนชื่อฉัน(ตัวอักษรพิมพ์ใหญ่)

    int age = 19;          // ตัวเลขเเทนอายุของฉัน

    float weight = 60.0;   // ตัวเลขเเทนน้ำหนักของฉัน(ทศนิยม1ตำเเหน่ง)



    // แสดงผลลัพธ์ออกหน้าจอคอมพิวเตอร์

    printf("Student %c is %d years old.\n", name, age);

    printf("His weighs is %.1f kg.\n", weight);



    return 0;

}